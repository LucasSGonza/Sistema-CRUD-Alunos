    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (this.jTable1.getSelectedRow() != -1) { //coluna -1 --> aparentemente é fora // coluna 0 --> id
            String nome = this.jTable1.getValueAt(this.jTable1.getSelectedRow(), 1).toString(); //getValueAt --> retorna o valor inserido na linha,coluna
            String idade = this.jTable1.getValueAt(this.jTable1.getSelectedRow(), 2).toString();
            String curso = this.jTable1.getValueAt(this.jTable1.getSelectedRow(), 3).toString();
            String fase = this.jTable1.getValueAt(this.jTable1.getSelectedRow(), 4).toString();

            this.textField_nome.setText(nome);
            this.textField_idade.setText(idade);
            this.textField_curso.setText(curso);
            this.textField_fase.setText(fase);
        }

    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_alterarActionPerformed
        // TODO add your handling code here:

        try {
            //Tratamento para caso de algum dado vazio + try catch para tratar erros            
            if (this.textField_nome.getText().length() == 0) {
                JOptionPane.showMessageDialog(null, "Nome não pode ser vazio!");
            } else if (this.textField_idade.getText().length() < 2) {
                JOptionPane.showMessageDialog(null, "Idade deve conter ao menos 2 caracteres!");
            } else if (this.textField_curso.getText().length() == 0) {
                JOptionPane.showMessageDialog(null, "Curso não pode ser vazio!");
            } else if (this.textField_fase.getText().length() == 0) {
                JOptionPane.showMessageDialog(null, "Fase não pode ser vazio!");
            } else {

                int id = Integer.parseInt(this.jTable1.getValueAt(this.jTable1.getSelectedRow(), 0).toString());
                String nome = this.textField_nome.getText();
                int idade = Integer.parseInt(this.textField_idade.getText());
                String curso = this.textField_curso.getText();
                int fase = Integer.parseInt(this.textField_fase.getText());

                if (control.editarAluno(curso, fase, id, nome, idade)) {
                    JOptionPane.showMessageDialog(null, "Aluno alterado com sucesso!");

                    //Limpa os dados
                    this.textField_nome.setText("");
                    this.textField_idade.setText("");
                    this.textField_curso.setText("");
                    this.textField_fase.setText("");
                    System.out.println(control.getListaAlunos().toString());
                    carregarTabela();
                    
                } else {
                    JOptionPane.showMessageDialog(null, "Erro!\nVerifique os dados inseridos e tente novamente");
                }

            }
        } catch (NumberFormatException a) {
            JOptionPane.showMessageDialog(null, "Verifique os dados inseridos novamente");
        } catch (ArrayIndexOutOfBoundsException b) {
            JOptionPane.showMessageDialog(null, "Primeiro deve-se cadastrar ou selecionar na tabela algum Aluno para então edita-lo");
        }

    }//GEN-LAST:event_jButton_alterarActionPerformed

