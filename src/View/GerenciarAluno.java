    public void carregarTabela() {

        DefaultTableModel modelo = (DefaultTableModel) this.jTable1.getModel();
        modelo.setNumRows(0);

        String[][] tabela = control.listaTabela();

        for (int i = 0; i < tabela.length; i++) {
            modelo.addRow(new Object[]{
                tabela[i][0],
                tabela[i][1],
                tabela[i][2],
                tabela[i][3],
                tabela[i][4],});
        }

    }

